package com.study.async;

public class HotPlace {
	
	private int hotplace_id;
	private String name;
	private String tel;
	private String addr;
	private String food;
	public int getHotplace_id() {
		return hotplace_id;
	}
	public void setHotplace_id(int hotplace_id) {
		this.hotplace_id = hotplace_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	
	
	

}
